n = 1000
a = [False,False] + [True]*(n-1)
primes = []

for i in range(2, n+1):
    if a[i]:
    # 처음에 2보다 큰 숫자들에 대해 True로 저장해 놓고, True들에 대해서 작업을 실행한다.

        primes.append(i)
        # True인 값들중 가장 작은 숫자를 primes 리스트에 append 한다.

        for j in range(2*i, n+1, i):
            a[j] = False
        # 해당 숫자, i로 나누어 지는 모든 숫자들 (자신을 제외한)을 False로 만든다.
        # 그렇다면 다음 루프에서 이 숫자들에 대한 연산은 시행하지 않는다.

print(primes)

