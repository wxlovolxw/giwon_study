# 나머지 값들 중에서 가장 작은 값을 선택한다. -> (3) 그다음 3으로 나누어지는 값을 모두 제거하고,
# 그 나머지 값들 중 가장 작은 5로 나누어지는 값들을 모두 제거. 이와 같은 식으로 계속 반복해 나간다.
